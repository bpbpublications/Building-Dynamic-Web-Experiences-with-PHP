<?php
// Using escape sequences in PHP

// Newline character
echo "Line 1\nLine 2\nLine 3\n";

// Tab character
echo "Column 1\tColumn 2\tColumn 3\n";

// Backslash character
echo "This is a backslash: \\ \n";

// Double quotes inside double quotes
echo "He said, \"Hello!\" to everyone.\n";

// Single quotes inside single quotes
echo 'She said, \'Hi!\' to her friend.\n';
?>